#include <stdio.h>

int main(void)
{
    // 정수 출력 폭 지정
    printf("%8d\n", 7629);  // 오른쪽 정렬, 폭 8
    printf("%-8d\n", 7629); // 왼쪽 정렬, 폭 8

    // 실수 출력 폭 지정
    printf("%10.3f\n", 32.369); // 소수점 이하 3자리, 전체 폭 10
    printf("%10f\n", 32.369);   // 소수점 이하 기본 6자리, 전체 폭 10

    // 문자열 출력 폭과 자릿수 지정
    printf("%10.3s\n", "Hello");  // 전체 폭 10, 앞에서 3글자만 출력
    printf("%-10.3s\n", "Hello"); // 전체 폭 10, 앞에서 3글자만 출력, 왼쪽 정렬

    // 기본 문자열 출력
    printf("%s\n", "Hello"); // 전체 출력

    // 문자열 출력 폭 지정
    printf("%10s\n", "Hello"); // 오른쪽 정렬, 폭 10

    // 문자열 출력 폭 + 일부 문자 출력
    printf("%10.5s\n", "Hello"); // 전체 폭 10, 최대 5글자 출력

    return 0;
}
